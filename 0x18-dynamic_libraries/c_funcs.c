#include <stdio.h>

/**
 * add - Adds two integers
 * @a: First Integer
 * @b: Second Integer
 * Return: The addition of both integers
 */

int add(int a, int b)
{
	return (a + b);
}


/**
 * sub - Subtarction
 * @a: First Integer
 * @b: Second Integer
 * Return: The subraction
 */

int sub(int a, int b)
{
	return (a - b);
}


/**
 * mul - Multiplies two integers
 * @a: First Integer
 * @b: Second Integer
 * Return: The multiplication of both integers
 */

int mul(int a, int b)
{
	return (a *b);
}


/**
 * div - Division
 * @a: First Integer
 * @b: Second Integer
 * Return: The result of the division
 */

int div(int a, int b)
{
	return (a / b);
}


/**
 * mod - The remainder when an integer divides another
 * @a: First Integer
 * @b: Second Integer
 * Return: The result
 */

int mod(int a, int b)
{
	return (a % b);
}

